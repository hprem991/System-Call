#define SYSCALL(name) \
  .globl name; \
  name: \
    movl $SYS_ ## name, %eax; \
    int $T_SYSCALL; \
    ret

#define CS550LSSYSCALL(name) \
  .globl name; \
  str_ ## name: \
    .string #name; \
  name :  movl $str_ ## name, %eax; \
    int $T_CS550LSSYSCALL; \
   ret

#define CS550HTSYSCALL(name) \
  .globl name; \
